package Chapter9;

public class Orange extends Fruit {
    public Orange(String shape, String color, int calories) {


        @Override
                public String toString; {
            return "Orange{" +
                    "shape='" + getShape() + '\'' +
                    ", color='" + getColor() + '\'' +
                    ", calories=" + getCalories() +
                    '}';
        }
    }
}
